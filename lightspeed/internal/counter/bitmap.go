package counter

const bitmapSize = 1 << 29

type bitmap struct {
	bits []byte
}

func newBitmap() *bitmap {
	return &bitmap{
		bits: make([]byte, bitmapSize),
	}
}

func (b *bitmap) set(pos uint32) {
	byteIndex := pos / 8
	bitOffset := pos % 8
	b.bits[byteIndex] |= 1 << bitOffset
}

func (b *bitmap) countSetBits() uint64 {
	var count uint64
	for _, byte := range b.bits {
		count += uint64(popCount(byte))
	}
	return count
}

